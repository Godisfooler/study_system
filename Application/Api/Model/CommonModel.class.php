    /**
     * Unicode编码转汉字
     * @param unknown $unistr
     * @param string $encoding
     * @param string $prefix
     * @param string $postfix
     * @return string
     */
    function unicode_decode($unistr, $encoding = 'utf-8', $prefix = '&#', $postfix = ';', $dechex = false) {
        if(substr_count($unistr, $prefix) > 0){
            $pattern = '/('.$prefix.'([\w'.$postfix.']{2,6}))/i';
            preg_match_all($pattern, $unistr, $arruni);
    
            for($i = 0, $len = count($arruni[2]); $i < $len; $i++) {
                if($dechex) $arruni[2][$i] = dechex($arruni[2][$i]);
                if (substr_count($arruni[2][$i], $postfix) > 0) {
                    $arruni[2][$i] = substr($arruni[2][$i], 0, strlen($arruni[2][$i]) - strlen($postfix));
                    // 								var_dump($arruni[2][$i]);
                }
    
                $temp = hexdec($arruni[2][$i]);
                $unidestr = ($temp < 256) ? chr(0) . chr($temp) : chr($temp / 256) . chr($temp % 256);
                $unidestr = iconv('UCS-2', $encoding, $unidestr);
                // 			var_dump($unidestr);
    
    
                $unistr = str_replace($arruni[0][$i], $unidestr, $unistr);
            }
        }
    
        return $this->html_decode($unistr);
    }
    
    /**
     * html编码转换
     * @param unknown $htmlStr
     */
    function html_decode($htmlStr){
        $htmlStr = trim($htmlStr);
        $table = get_html_translation_table(HTML_ENTITIES);
    
        foreach ($table as $key=>$html){
            if(substr_count($htmlStr, $html) > 0){
                $htmlStr = str_replace($html, $key, $htmlStr);
            }
        }
    
        return $htmlStr;
    }
    
    
